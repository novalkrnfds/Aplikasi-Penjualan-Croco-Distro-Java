    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        if(petugasTable.getSelectedRowCount() > 0){
            String pilihPetugas = petugasTableModel.getValueAt(petugasTable.getSelectedRow(), 1).toString();
            petugasController.ubah(pilihPetugas);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Belum ada yang dipilih", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if(petugasTable.getSelectedRowCount() > 0){
            String namaPetugas = petugasTableModel.getValueAt(petugasTable.getSelectedRow(), 1).toString();
            if(petugasController.delete(namaPetugas)){
                petugasTableModel.removeRow(petugasTable.getSelectedRow());
            } else{
                //JOptionPane.showMessageDialog(null, perusahaan.getPesan());
            }
        } else{
            JOptionPane.showMessageDialog(rootPane, "Belum ada yang dipilih", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

