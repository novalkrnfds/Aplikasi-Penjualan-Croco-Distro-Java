    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if(kategoriBarangTable.getSelectedRowCount() > 0){
            String nama = kategoriTableModel.getValueAt(kategoriBarangTable.getSelectedRow(), 1).toString();
            if(kategoriController.delete(nama)){
                kategoriTableModel.removeRow(kategoriBarangTable.getSelectedRow());
            } else{
                //JOptionPane.showMessageDialog(null, perusahaan.getPesan());
            }
        } else{
            JOptionPane.showMessageDialog(rootPane, "Belum ada yang dipilih", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


