    public static void tampilkanData(Object[][] list){
        kategoriTableModel.setRowCount(0);
        if((list != null) && (list.length>0)){
            for (int i=0;i<list.length;i++){
               kategoriTableModel.addRow(list[i]);
            }
        }
    }
    
