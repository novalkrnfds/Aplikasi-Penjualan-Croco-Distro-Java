    private void tampilData() {
        try {
            //Looping Tabel
            penjualanTable.getColumnModel().getColumn(3).setCellRenderer(new Currency());
            int baris = penjualanTable.getSelectedRow();

            for (int i = 0; i < baris; i++) {
                penjualanTableModel.removeRow(i);
            }

            
                String sql = "select tbdetailpenjualan.kodebarang,tbbarang.namabarang,tbdetailpenjualan.jumlah,tbdetailpenjualan.sub "
                        + "from tbdetailpenjualan,tbbarang where tbdetailpenjualan.kodebarang=tbbarang.kodebarang and "
                        + "tbdetailpenjualan.no_faktur='"+notaTextField.getText()+"'";
                Statement statement = conn.connection.createStatement();
                //con.connection.createStatement();
                ResultSet resultset = statement.executeQuery(sql);

                while (resultset.next()) {
                    String a = resultset.getString(1);
                    String b = resultset.getString(2);
                    String c = resultset.getString(3);
                    String d = resultset.getString(4);

                    Object isi[] = {a,b,c,d};
                    penjualanTableModel.addRow(isi);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public static void tampilkanData(Object[][] list){
        penjualanTableModel.setRowCount(0);
        if((list != null) && (list.length>0)){
            for(int i=0;i<list.length;i++){
                penjualanTableModel.addRow(list[i]);
            }
        }
    }
    
    private void autoFaktur(){
        try{
            sta = conn.getKoneksi().createStatement();
            String sql = "select * from tbpenjualan order by no_faktur desc";
            rset = sta.executeQuery(sql);
            if(rset.next()){
                String id = rset.getString("no_faktur").substring(1);
                String a = "" + (Integer.parseInt(id) + 1);
                String nol = "";
                
                if(a.length()==1){
                    nol = "000";
                } else if(a.length()==2){
                    nol = "00";
                } else if(a.length()==3){
                    nol = "0";
                } else if(a.length()==4){
                    nol = "";
                }
                notaTextField.setText("F" + nol + a);
            } else {
                notaTextField.setText("F0001");
            }
            rset.close();
        } catch(Exception ex){
            ex.printStackTrace();
        }
    }
    
