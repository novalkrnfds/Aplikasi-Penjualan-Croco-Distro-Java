    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
        // TODO add your handling code here:
        if(!kdTextField.getText().equals("")){
            if(!jmlSpinner.getValue().equals(0)){
                Double a,b;
                a = Double.parseDouble(stokTextField.getText());
                b = Double.parseDouble(jmlSpinner.getValue().toString());
                
                if(a<b){
                    JOptionPane.showMessageDialog(null, "Maaf, Stok Tidak Mencukupi");
                } else {
                    
                    subTransaksi();
                
                    int rows = penjualanTable.getRowCount();
                    int sum=0;
                    for(int x=0;x<rows;x++){
                        String sub = penjualanTable.getValueAt(x, 3).toString();
                        int total = Integer.parseInt(sub);
                        sum = sum + total;
                    }
                    DecimalFormat df = new DecimalFormat("#.##");
                    String sum1 = Integer.toString(sum);
                    txtsub.setText(df.format(sum));
                    bayarTextField.setEnabled(true);
                    hitungButton.setEnabled(true);
                    selesaiButton.setEnabled(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Anda belum menginput jumlah pembelian", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Lengkapi data terlebih dahulu", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tambahButtonActionPerformed

