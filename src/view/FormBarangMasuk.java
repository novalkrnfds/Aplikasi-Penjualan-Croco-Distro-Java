    private void addItem(){
        try{
            sta = conn.getKoneksi().createStatement();
            String sql = "insert into tbdetailbrgmasuk values('"+notaTextField.getText()+"','"+barangComboBox.getSelectedItem()+"',"
                    + "'"+qtyTextField.getText()+"','"+hrgTextField.getText()+"','"+subTextField.getText()+"','"+ketArea.getText()+"')";
            sta.executeUpdate(sql);
            
            Statement st = conn.getKoneksi().createStatement();
            String SQL = "update tbbarang set jumlah = jumlah + '"+qtyTextField.getText()+"' where kodebarang = '"+barangComboBox.getSelectedItem().toString()+"'";
            st.executeUpdate(SQL);
            sta.close();
            BacaTabel();
            bersihDetail();
        }catch(Exception ex){}
    }
    
    private void BacaTabel() {
        try {
            //Looping Tabel
            int baris = masukTable.getSelectedRow();

            for (int i = 0; i < baris; i++) {
                masukTableModel.removeRow(i);
            }

            
                String sql = "select no_nota,kodebarang,jumlah,harga,subtotal,keterangan from tbdetailbrgmasuk where no_nota='"+notaTextField.getText()+"'";
                Statement statement = conn.connection.createStatement();
                //con.connection.createStatement();
                ResultSet resultset = statement.executeQuery(sql);

                while (resultset.next()) {
                    String a = resultset.getString(1);
                    String b = resultset.getString(2);
                    String c = resultset.getString(3);
                    String d = resultset.getString(4);
                    String e = resultset.getString(5);
                    String f = resultset.getString(6);

                    Object isi[] = {a,b,c,d,e,f};
                    masukTableModel.addRow(isi);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
