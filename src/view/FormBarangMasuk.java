    private void autoNota(){
        try{
            sta = conn.getKoneksi().createStatement();
            String sql = "select * from tbbarangmasuk order by no_nota desc";
            rset = sta.executeQuery(sql);
            if(rset.next()){
                String id = rset.getString("no_nota").substring(1);
                String a = "" + (Integer.parseInt(id) + 1);
                String nol = "";
                
                if(a.length()==1){
                    nol = "000";
                } else if(a.length()==2){
                    nol = "00";
                } else if(a.length()==3){
                    nol = "0";
                } else if(a.length()==4){
                    nol = "";
                }
                notaTextField.setText("N" + nol + a);
            } else {
                notaTextField.setText("N0001");
            }
            rset.close();
        } catch(Exception ex){
            ex.printStackTrace();
        }
    }
    
