    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
        // TODO add your handling code here:
        
        if(!namaTextField.getText().equals("")){
            addItem();
            int rows = masukTable.getRowCount();
            double sum=0;
            for(int x=0;x<rows;x++){
                String sub = masukTable.getValueAt(x, 4).toString();
                int total = Integer.parseInt(sub);
                sum = sum + total;
            }
            String sum1 = Double.toString(sum);
            totalTextField.setText(sum1);
            saveButton.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Lengkapi data terlebih dahulu", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tambahButtonActionPerformed

